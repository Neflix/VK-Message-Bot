abstract class FileUtils
{
	/**
	 *  Возвращает очищенный путь от корневых папок.
	 *
	 *  @param string $path
	 *
	 *  @return string
	 */
	static function getCleanedPath (string $path) : string
	{
		return DIRECTORY_SEPARATOR.str_replace(VKBOT_DIR, null, $path);
	}
}